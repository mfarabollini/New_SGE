        private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
            Icon Icon = null;
            boolean continuar = true;

            if (modo == 1){
                if(this.getTxtCuit().getText().trim().length()==0){
                    JOptionPane.showMessageDialog(this, "Debe ingresar el CUIT", "Alta Cliente", JOptionPane.ERROR_MESSAGE,   Icon);
                    continuar=false;
                    this.getTxtCuit().requestFocus();
                }
                if(this.getTxtRazonSocial().getText().trim().length()==0 && continuar){
                    JOptionPane.showMessageDialog(this, "Debe informar la Razon Social", "Alta Cliente", JOptionPane.ERROR_MESSAGE,   Icon);
                    continuar=false;
                    this.getTxtRazonSocial().requestFocus();
                }
                if(this.getTxtDir().getText().trim().length()==0 && continuar){
                    JOptionPane.showMessageDialog(this, "Debe ingresar la dirección", "Alta Cliente", JOptionPane.ERROR_MESSAGE,   Icon);
                    continuar=false;
                    this.getTxtDir().requestFocus();
                }
                if(this.getCboLocalidad().getSelectedIndex() ==-1 && continuar){
                    JOptionPane.showMessageDialog(this, "Debe ingresar la localidad", "Alta Cliente", JOptionPane.ERROR_MESSAGE,   Icon);
                    continuar=false;
                    this.getCboLocalidad().requestFocus();
                }                  
            }else if(modo == 2){
                if(this.getTxtCuit().getText().trim().length()==0){
                    JOptionPane.showMessageDialog(this, "Debe ingresar el CUIT", "Modificar Cliente", JOptionPane.ERROR_MESSAGE,   Icon);
                    continuar=false;
                    this.getTxtCuit().requestFocus();
                }
                if(this.getTxtRazonSocial().getText().trim().length()==0 && continuar){
                    JOptionPane.showMessageDialog(this, "Debe informar la Razon Social", "Modificar Cliente", JOptionPane.ERROR_MESSAGE,   Icon);
                    continuar=false;
                    this.getTxtRazonSocial().requestFocus();
                }
                if(this.getTxtDir().getText().trim().length()==0 && continuar){
                    JOptionPane.showMessageDialog(this, "Debe ingresar la dirección", "Modificar Cliente", JOptionPane.ERROR_MESSAGE,   Icon);
                    continuar=false;
                    this.getTxtDir().requestFocus();
                }
                if(this.getCboLocalidad().getSelectedIndex() ==-1 && continuar){
                    JOptionPane.showMessageDialog(this, "Debe ingresar la localidad", "Modificar Cliente", JOptionPane.ERROR_MESSAGE,   Icon);
                    continuar=false;
                    this.getCboLocalidad().requestFocus();
                }  
            }else{
                if(this.getTxtCuit().getText().trim().length()==0){
                    JOptionPane.showMessageDialog(this, "Debe seleccionar un cliente", "Borrar Cliente", JOptionPane.ERROR_MESSAGE,   Icon);
                    continuar=false;
                    this.getTxtCuit().requestFocus();
                }
            }
            if(continuar){
                presenter.getPres().stateChanged(new ChangeEvent(this));
            }
        }
        
