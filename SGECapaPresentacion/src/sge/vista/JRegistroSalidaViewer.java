    private void txtNumeroEnvioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtNumeroEnvioActionPerformed
        // TODO add your handling code here:
        if(this.txtNumeroEnvio.getText().trim().length()>= 0){            
            GenericoTableModel aModel= (GenericoTableModel) this.tblEnvio.getModel();
            aModel.limpiar();  
            
            this.tblEnvio.addNotify();
            presenter.getBuscarEnvioHandler().stateChanged(new ChangeEvent(this));
            tblEnvio.changeSelection(0, 1, false, false);
            this.cmdEnviar.requestFocus();
        }else{
            this.txtNumeroEnvio.requestFocus();
        }
            
    }//GEN-LAST:event_txtNumeroEnvioActionPerformed

    private void txtNumeroEnvioKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNumeroEnvioKeyTyped
        // TODO add your handling code here:
      char caracter = evt.getKeyChar();
      // Verificar si la tecla pulsada no es un digito
      if( !Character.isDigit(caracter)){      
         evt.consume();  // ignorar el evento de teclado
      }
    }

