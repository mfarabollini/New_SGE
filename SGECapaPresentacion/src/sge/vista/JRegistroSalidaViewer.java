        private void cmdCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdCancelarActionPerformed
            this.txtNumeroEnvio.setText("");
            this.txtFechaViaje.setText("");
            this.txtRazonSocial.setText("");
            GenericoTableModel aModel= (GenericoTableModel) this.tblEnvio.getModel();
            aModel.limpiar();  
            this.tblEnvio.addNotify();        
            this.txtNumeroEnvio.requestFocus();
            presenter.getCancelarEnvioHandler().stateChanged(new ChangeEvent(this));
            
        }
        // TODO add your handling code here:}//GEN-LAST:event_cmdCancelarActionPerformed

        private void cmdEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdEnviarActionPerformed
            
            if(this.tblEnvio.getRowCount()> 0){
                presenter.getRegistrarDespachoHandler().stateChanged(new ChangeEvent(this));
                this.txtNumeroEnvio.setText("");
                this.txtFechaViaje.setText("");
                this.txtRazonSocial.setText("");
                GenericoTableModel aModel= (GenericoTableModel) this.tblEnvio.getModel();
                aModel.limpiar();  
                this.tblEnvio.addNotify();        
                this.txtNumeroEnvio.requestFocus();
                
            }else{
                
                notificarMensaje("No ha buscado el envio a despachar", JOptionPane.INFORMATION_MESSAGE);
            }
            
        }    
        // TODO add your handling code here:     }//GEN-LAST:event_cmdEnviarActionPerformed

